#include <unistd.h>
int ft_putchar(char c)
{

	write(1, &c, 1);

}

void ft_print_comb(void)

{
	char nbr_1;
	char nbr_2;
	char nbr_3;

	nbr_1 = '0'- 1;
	while(++nbr_1 <= '9')
	{
		nbr_2 = nbr_1;
		while(++nbr_2 <= '9')
		{ 
			nbr_3 = nbr_2;
			while(++nbr_3 <= '9')
			{ 
				if ( nbr_1 != '0' || nbr_2 != '1' || nbr_3 != '2' )
					ft_putchar (',');

				if ( nbr_1 != '0' || nbr_2 != '1' || nbr_3 != '2')
					ft_putchar (' ');
				ft_putchar (nbr_1);
				ft_putchar (nbr_2);
				ft_putchar (nbr_3);
			}
		}
	}
	ft_putchar('\n');
}

